    .section .data
value:
    .word 0x12345678  # Значение для загрузки и сохранения

    .section .text
    .globl _start

_start:
    # Загрузка и сохранение данных
    la t0, value      # Загружаем адрес value в t0
    lw t1, 0(t0)      # Загружаем значение из памяти в t1
    addi t1, t1, 1    # Увеличиваем значение на 1
    sw t1, 0(t0)      # Сохраняем значение обратно в память

    # Завершаем программу
    li a7, 93         # Системный вызов exit
    li a0, 0          # Код завершения 0
    ecall
